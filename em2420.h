#ifndef _EM2420_H
#define _EM2420_H

#define EM2420_STROBE_SNOP 0x00
#define EM2420_STROBE_SXOSCON 0x01
#define EM2420_STROBE_STXCAL 0x02
#define EM2420_STROBE_SRXON 0x03
#define EM2420_STROBE_STXON 0x04
#define EM2420_STROBE_STXONCCA 0x05
#define EM2420_STROBE_SRFOFF 0x06
#define EM2420_STROBE_SXOSCOFF 0x07
#define EM2420_STROBE_SFLUSHRX 0x08
#define EM2420_STROBE_SFLUSHTX 0x09
#define EM2420_STROBE_SACK 0x0A
#define EM2420_STROBE_SACKPEND 0x0B
#define EM2420_STROBE_SRXDEC 0x0C
#define EM2420_STROBE_STXENC 0x0D
#define EM2420_STROBE_SAES 0x0E
// Register 0x0F is not used
#define EM2420_REGISTER_MAIN 0x10
#define EM2420_REGISTER_MDMCTRL0 0x11
#define EM2420_REGISTER_MDMCTRL1 0x12
#define EM2420_REGISTER_RSSI 0x13
#define EM2420_REGISTER_SYNCWORD 0x14
#define EM2420_REGISTER_TXCTRL 0x15
#define EM2420_REGISTER_RXCTRL0 0x16
#define EM2420_REGISTER_RXCTRL1 0x17
#define EM2420_REGISTER_FSCTRL 0x18
#define EM2420_REGISTER_SECCTRL0 0x19
#define EM2420_REGISTER_SECCTRL1 0x1A
#define EM2420_REGISTER_BATTMON 0x1B
#define EM2420_REGISTER_IOCFG0 0x1C
#define EM2420_REGISTER_IOCFG1 0x1D
#define EM2420_REGISTER_MANFIDL 0x1E
#define EM2420_REGISTER_MANFIDH 0x1F
#define EM2420_REGISTER_FSMTC 0x20
#define EM2420_REGISTER_MANAND 0x21
#define EM2420_REGISTER_MANOR 0x22
#define EM2420_REGISTER_AGCCTRL 0x23
#define EM2420_REGISTER_AGCTST0 0x24
#define EM2420_REGISTER_AGCTST1 0x25
#define EM2420_REGISTER_AGCTST2 0x26
#define EM2420_REGISTER_FSTST0 0x27
#define EM2420_REGISTER_FSTST1 0x28
#define EM2420_REGISTER_FSTST2 0x29
#define EM2420_REGISTER_FSTST3 0x2A
#define EM2420_REGISTER_RXBPFTST 0x2B
#define EM2420_REGISTER_FSMSTATE 0x2C
#define EM2420_REGISTER_ADCTST 0x2D
#define EM2420_REGISTER_DACTST 0x2E
#define EM2420_REGISTER_TOPTST 0x2F
// Register 0x30 is reserved
// Registers 0x31-0x3D are not used
#define EM2420_REGISTER_TXFIFO 0x3E
#define EM2420_REGISTER_RXFIFO 0x3F

void em2420_init();
uint8_t em2420_send_command_strobe(uint8_t address);
uint8_t em2420_read_register(uint8_t address, uint16_t *register_value);

#endif
